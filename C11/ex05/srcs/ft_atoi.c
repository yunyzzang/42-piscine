int	ft_atoi(char *str)
{	
	int	sign;
	int	value;

	sign = 1;
	value = 0;
	while (*str < 33 || *str == 127)
	{
		str++;
	}
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
		{
			sign *= -1;
		}
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		value = value * 10 + (*str - '0');
		str++;
	}
	return (sign * value);
}	
