void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	rev;

	i = 0;
	while (i < size)
	{
		rev = tab[i];
		tab[i] = tab[size - 1];
		tab[size - 1] = rev;
		i++;
		size--;
	}
}
