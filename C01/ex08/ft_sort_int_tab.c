void	ft_sort_int_tab(int *tab, int size)
{
	int	a;
	int	b;
	int	temp;

	a = 0;
	while (a < size)
	{
		b = a + 1;
		while (b < size)
		{
			if (tab[b] <= tab[a])
			{
				temp = tab[a];
				tab[a] = tab[b];
				tab [b] = temp;
			}
			b++;
		}
		a++;
	}
}
